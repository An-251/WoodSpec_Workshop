function normalizeText(value) {
  return value
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
}

export function rankPromptSuggestions(input) {
  const normalizedInput = normalizeText(input)

  return promptSuggestions
    .map((suggestion) => {
      const matchedKeywords = suggestion.keywords.filter((keyword) => normalizedInput.includes(normalizeText(keyword)))
      const score = normalizedInput.trim() ? matchedKeywords.length : 0

      return {
        ...suggestion,
        matchedKeywords,
        score,
        confidence: Math.min(96, Math.max(score > 0 ? 58 : 38, 38 + score * 14)),
      }
    })
    .sort((first, second) => {
      if (second.score !== first.score) {
        return second.score - first.score
      }

      return first.label.localeCompare(second.label, "vi")
    })
}

export function findPromptSuggestion(input) {
  return rankPromptSuggestions(input)[0] ?? promptSuggestions[0]
}
